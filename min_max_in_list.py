# python has min and max methods that give minimum and maximum number in a list

def minimum():
    numbers = [2, 4, 8, 4, 23, 5, 6]
    # display minimum in this list
    print('the minimum number in the list is : ', min(numbers))

minimum()

# method for maximum
def maximum():
    numbers = [2, 4, 8, 4, 23, 5, 6]
    print('the maximum number in the list is : ', max(numbers))
maximum()